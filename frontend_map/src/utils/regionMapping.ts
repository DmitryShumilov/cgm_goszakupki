/**
 * Обратное сопоставление: База данных → GeoJSON
 */
export const reverseRegionMapping: Record<string, string> = Object.entries(regionMapping)
  .reduce((acc, [geojson, db]) => {
    acc[db] = geojson;
    return acc;
  }, {} as Record<string, string>);

/**
 * Функция для нормализации названия региона из GeoJSON к виду в БД
 */
export function normalizeRegionName(geojsonName: string): string {
  return regionMapping[geojsonName] || geojsonName;
}

/**
 * Функция для обратного преобразования: из БД в GeoJSON
 */
export function toGeojsonName(dbName: string): string {
  return reverseRegionMapping[dbName] || dbName;
}

/**
 * Функция для получения списка всех регионов GeoJSON
 */
export function getGeojsonRegions(): string[] {
  return Object.keys(regionMapping);
}

/**
 * Функция для получения списка всех регионов БД
 */
export function getDbRegions(): string[] {
  return Object.values(regionMapping);
}
